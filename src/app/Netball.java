    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Uni_ID = jTextField1.getText();
        String Type = jComboBox1.getSelectedItem().toString();
        String Matches = jTextField2.getText();
        String Position = jComboBox2.getSelectedItem().toString();
        String Oposite_team = jTextField3.getText();

        try {

            Statement stmt = new DAC().getConnection().createStatement();
            if (!(jTextField1.getText().isEmpty())) {
                String query1 = "SELECT University_ID FROM playerdetails WHERE University_ID ='" + Uni_ID + "'";
                ResultSet rs = stmt.executeQuery(query1);
                if (rs.next()) {

                    String query = "INSERT INTO netballl (University_ID,type,matches,position,Oposite_team)VALUES('" + Uni_ID + "','" + Type + "','" + Matches + "','" + Position + "','" + Oposite_team + "')";

                    int a = stmt.executeUpdate(query);
                    if (a > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Sucessfully ");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, " non Sucessfully ");
                    }
                    ResultSet rs1 = stmt.executeQuery("SELECT University_ID,type,Oposite_team,matches,position FROM netballl");
                    DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
                    while (rs1.next()) {
                        Vector v = new Vector();
                        v.add(rs1.getString(1));
                        v.add(rs1.getString(2));
                        v.add(rs1.getString(3));
                        v.add(rs1.getString(4));
                        v.add(rs1.getString(5));
                        tb.addRow(v);
                    }

                } else {
                    JOptionPane.showMessageDialog(rootPane, " Your profile is not ready yet.. ");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

